use rand::Rng;

pub fn random() -> String {
    let mut rng = rand::thread_rng();
    let mut out = "".to_string();
    for _ in 0..20 {
        for _ in 0..40 {
            out.push_str(if rng.gen_range(0..=1) == 1 { "O" } else { "." })
        }
        out.push('\n')
    }
    out.trim_end().to_string()
}

